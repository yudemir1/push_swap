#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	if (!dst && !src)
		return (NULL);
	if (dst > src)
		while (len--)
			*(d + len) = *(s + len);
	if (dst < src)
		ft_memcpy(dst, src, len);
	return (dst);
}
