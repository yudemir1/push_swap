#include "libft.h"

int	ft_atoi(const char *str)
{
	int	n;
	int	x;

	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	n = 0;
	x = 1;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			x *= -1;
		str++;
	}
	while (*str <= '9' && *str >= '0')
	{
		n *= 10;
		n += *str - 48;
		str++;
	}
	return (n * x);
}
